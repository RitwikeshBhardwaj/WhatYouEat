import PDFDocument from 'pdfkit';
import Meal from '../models/Meal.js';
import WaterLog from '../models/WaterLog.js';
import DailyGoal from '../models/DailyGoal.js';
import { startOfUTCDay, addDays, startOfWeekUTC } from '../utils/dates.js';

const fmt = (d) => d.toISOString().slice(0, 10);

export const exportWeeklyPdf = async (req, res, next) => {
  try {
    const end = req.query.to ? startOfUTCDay(new Date(req.query.to)) : startOfUTCDay(new Date());
    const start = req.query.from ? startOfUTCDay(new Date(req.query.from)) : startOfWeekUTC(end);

    const byDay = await Meal.aggregate([
      { $match: { user: req.user._id, date: { $gte: start, $lte: end } } },
      {
        $group: {
          _id: '$date',
          calories: { $sum: '$nutrition.calories' },
          protein: { $sum: '$nutrition.protein' },
          carbs: { $sum: '$nutrition.carbs' },
          fat: { $sum: '$nutrition.fat' },
          count: { $sum: 1 },
        },
      },
      { $sort: { _id: 1 } },
    ]);

    const water = await WaterLog.find({ user: req.user._id, date: { $gte: start, $lte: end } });
    const waterMap = new Map(water.map((w) => [w.date.getTime(), w.glasses]));
    const goals = await DailyGoal.find({ user: req.user._id, date: { $gte: start, $lte: end } });
    const goalMap = new Map(goals.map((g) => [g.date.getTime(), g.calorieGoal]));

    const days = [];
    for (let i = 0; i < 7; i++) {
      const d = addDays(start, i);
      const found = byDay.find((b) => new Date(b._id).getTime() === d.getTime());
      days.push({
        date: d,
        calories: found ? Math.round(found.calories) : 0,
        protein: found ? Math.round(found.protein * 10) / 10 : 0,
        carbs: found ? Math.round(found.carbs * 10) / 10 : 0,
        fat: found ? Math.round(found.fat * 10) / 10 : 0,
        count: found ? found.count : 0,
        water: waterMap.get(d.getTime()) || 0,
        goal: goalMap.get(d.getTime()) || 2000,
      });
    }

    const totals = days.reduce(
      (a, d) => ({
        calories: a.calories + d.calories,
        protein: a.protein + d.protein,
        carbs: a.carbs + d.carbs,
        fat: a.fat + d.fat,
        water: a.water + d.water,
      }),
      { calories: 0, protein: 0, carbs: 0, fat: 0, water: 0 }
    );

    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="whatyoueat-${fmt(start)}-to-${fmt(end)}.pdf"`);

    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    doc.pipe(res);

    doc.fontSize(22).fillColor('#4f46e5').text('WhatYouEat', { continued: true });
    doc.fillColor('#111').fontSize(14).text('  Weekly Nutrition Summary');
    doc.moveDown(0.3);
    doc.fontSize(10).fillColor('#666').text(`${fmt(start)} → ${fmt(end)}`);
    doc.moveDown(1);

    doc.fillColor('#111').fontSize(13).text('Weekly Totals');
    doc.moveDown(0.3);
    doc.fontSize(11).fillColor('#333');
    doc.text(`Calories: ${Math.round(totals.calories)} kcal  (avg ${Math.round(totals.calories / 7)}/day)`);
    doc.text(`Protein: ${Math.round(totals.protein)} g   Carbs: ${Math.round(totals.carbs)} g   Fat: ${Math.round(totals.fat)} g`);
    doc.text(`Water: ${totals.water} glasses  (avg ${Math.round((totals.water / 7) * 10) / 10}/day)`);
    doc.moveDown(1);

    doc.fillColor('#111').fontSize(13).text('Daily Breakdown');
    doc.moveDown(0.3);
    const tableTop = doc.y;
    const colX = [50, 160, 250, 320, 390, 460, 520];
    doc.fontSize(9).fillColor('#666');
    ['Date', 'Calories', 'Goal', 'Protein', 'Carbs', 'Fat', 'Water'].forEach((h, i) => {
      doc.text(h, colX[i], tableTop);
    });
    doc.moveTo(50, tableTop + 14).lineTo(550, tableTop + 14).strokeColor('#ddd').lineWidth(1).stroke();
    let y = tableTop + 22;
    doc.fontSize(10).fillColor('#222');
    days.forEach((d) => {
      const pct = d.goal ? Math.round((d.calories / d.goal) * 100) : 0;
      doc.text(fmt(d.date), colX[0], y);
      doc.text(`${d.calories}`, colX[1], y);
      doc.text(`${d.goal} (${pct}%)`, colX[2], y);
      doc.text(`${d.protein}g`, colX[3], y);
      doc.text(`${d.carbs}g`, colX[4], y);
      doc.text(`${d.fat}g`, colX[5], y);
      doc.text(`${d.water}`, colX[6], y);
      y += 18;
    });

    doc.moveDown(2);
    doc.fontSize(9).fillColor('#999').text('Generated by WhatYouEat', { align: 'center' });

    doc.end();
  } catch (err) {
    next(err);
  }
};
